package com.space.infra.module.member;

public class Member {
	
	private String mmSeq;
	private String mmName;
	private String mmId;
	private String mmGender;
	private String mmAge;
	private String mmPhoneNumber;
	private String mmEmail;
	private String mmPassword;
	
	
	public String getMmSeq() {
		return mmSeq;
	}
	public void setMmSeq(String mmSeq) {
		this.mmSeq = mmSeq;
	}
	public String getMmName() {
		return mmName;
	}
	public void setMmName(String mmName) {
		this.mmName = mmName;
	}
	public String getMmId() {
		return mmId;
	}
	public void setMmId(String mmId) {
		this.mmId = mmId;
	}
	public String getMmGender() {
		return mmGender;
	}
	public void setMmGender(String mmGender) {
		this.mmGender = mmGender;
	}
	public String getMmAge() {
		return mmAge;
	}
	public void setMmAge(String mmAge) {
		this.mmAge = mmAge;
	}
	public String getMmPhoneNumber() {
		return mmPhoneNumber;
	}
	public void setMmPhoneNumber(String mmPhoneNumber) {
		this.mmPhoneNumber = mmPhoneNumber;
	}
	public String getMmEmail() {
		return mmEmail;
	}
	public void setMmEmail(String mmEmail) {
		this.mmEmail = mmEmail;
	}
	public String getMmPassword() {
		return mmPassword;
	}
	public void setMmPassword(String mmPassword) {
		this.mmPassword = mmPassword;
	}

}
