package com.space.infra.modules.codegroup;

import com.space.infra.modules.base.Base;

public class CodeGroup extends Base{

	
	private Integer cgSeq;
	private String cgName;
	private Integer cgDelNy;
	private Integer cgUseNy;
	private String cgCreateDate;
	
	public Integer getCgSeq() {
		return cgSeq;
	}
	public void setCgSeq(Integer cgSeq) {
		this.cgSeq = cgSeq;
	}
	public String getCgName() {
		return cgName;
	}
	public void setCgName(String cgName) {
		this.cgName = cgName;
	}
	public Integer getCgDelNy() {
		return cgDelNy;
	}
	public void setCgDelNy(Integer cgDelNy) {
		this.cgDelNy = cgDelNy;
	}
	public Integer getCgUseNy() {
		return cgUseNy;
	}
	public void setCgUseNy(Integer cgUseNy) {
		this.cgUseNy = cgUseNy;
	}
	public String getCgCreateDate() {
		return cgCreateDate;
	}
	public void setCgCreateDate(String cgCreateDate) {
		this.cgCreateDate = cgCreateDate;
	}
	
	

	
}
